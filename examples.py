from graph import Graph

def example1():
    
    '''simple symmetry'''
    
    g1 = {'1': ['2','3'],
          '2': ['1','3','4','5'],
          '3': ['1','2','4','5'],
          '4': ['2','3','5'],
          '5': ['2','3','4']}
    
    g2 = {'a': ['b','c'],
          'b': ['a','c','d','e'],
          'c': ['a','b','d','e'],
          'd': ['b','c','e'],
          'e': ['b','c','d']}
    
    return Graph(g1,g2)

def example2():
    
    '''double symmetry'''

    g = {'a': ['b','c'],
         'b': ['a','c','e'],
         'c': ['a','b','d'],
         'd': ['c','e','f'],
         'e': ['b','d','f'],
         'f': ['d','e']}

    return Graph(g)

def example3():
    
    '''multiple symmetry'''
    
    g = {'a': ['b','e','f'],
         'b': ['a','c','g'],
         'c': ['b','d','h'],
         'd': ['c','e','i'],
         'e': ['a','d','j'],
         'f': ['a','h','i'],
         'g': ['b','i','j'],
         'h': ['c','f','j'],
         'i': ['d','f','g'],
         'j': ['e','g','h']}
    
    return Graph(g)

